package model;

import java.util.Date;

/**
 *
 * @author Jessie
 */
public class Comment {
    private int id;
    private String content;
    private Date time;
    private Status status;
    private User user;

    public Comment() {
    }

    public Comment(int id, String content, Date time, Status status, User user) {
        this.id = id;
        this.content = content;
        this.time = time;
        this.status = status;
        this.user = user;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getContent() {
        return content;
    }

    public void setContent(String content) {
        this.content = content;
    }

    public Date getTime() {
        return time;
    }

    public void setTime(Date time) {
        this.time = time;
    }

    public Status getStatus() {
        return status;
    }

    public void setStatus(Status status) {
        this.status = status;
    }

    public User getUser() {
        return user;
    }

    public void setUser(User user) {
        this.user = user;
    }

    
}
